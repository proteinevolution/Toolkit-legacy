inline char i2aa(char c)
{
  //A  R  N  D  C  Q  E  G  H  I  L  K  M  F  P  S  T  W  Y  V
  switch (c)
    {
    case 0: return 'A';
    case 1: return 'R';
    case 2: return 'N';
    case 3: return 'D';
    case 4: return 'C';
    case 5: return 'Q';
    case 6: return 'E';
    case 7: return 'G';
    case 8: return 'H';
    case 9: return 'I';
    case 10: return 'L';
    case 11: return 'K';
    case 12: return 'M';
    case 13: return 'F';
    case 14: return 'P';
    case 15: return 'S';
    case 16: return 'T';
    case 17: return 'W';
    case 18: return 'Y';
    case 19: return 'V';
    case 20: return 'X';
    case 21: return '-';
    case 22: return '-';
    }
  return '?';
}

// // FRcons function
// float FRcons(int b, float Rtrb[], float x) 
// {
//   float numerator=0.0;
//   float denominator=0.0;
//   float Rpc[20];
//   int a;
//   for (a=0; a<20; ++a) Rpc[a] = x*Rtrb[a];
//   Rpc[b] += (1-x); 
//   for (a=0; a<20; ++a) numerator += Rpc[a]*Rpc[a]/pb[a];
//   for (a=0; a<20; ++a) denominator += Rpc[a]/pb[a];
//   return log(numerator)/log(denominator);
// }

// FRcons function
float FRcons(int b, float Rtrb[], float x) 
{
  float numerator=0.0;
  float denominator=0.0;
  float Rpc[20];
  int a;
  if (x<=0) x=1E-4;
  for (a=0; a<20; ++a) Rpc[a] = x*Rtrb[a];
  Rpc[b] += (1-x); 
  for (a=0; a<20; ++a) numerator += Rpc[a]*log(Rpc[a]);
  for (a=0; a<20; ++a) denominator += Rpc[a]*log(pb[a]);
  return 1-numerator/denominator;
}



