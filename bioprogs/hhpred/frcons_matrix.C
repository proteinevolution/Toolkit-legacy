void SetBlosumMatrix(const float BlosumXX[])
{
  int a,b,n=0;
  if (v>=3) printf("Using the BLOSUM%2i matrix",matrix);
  for (a=0; a<20; ++a)
    for (b=0; b<=a; ++b,++n)
      P[a][b] = P[b][a] = BlosumXX[n];
  for (a=0; a<20; ++a) P[a][20]=P[20][a]=1.0f;
  return;
}

